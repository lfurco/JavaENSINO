// Cria um array vazio (simulando uma pilha)
let pilha = []

// Adiciona elementos no FINAL do array
pilha.push(10)
pilha.push(15)
pilha.push(20)
pilha.push(25)
pilha.push(30)

// Mostra o array após as inserções
console.log(pilha)


// Adiciona um elemento no INÍCIO do array
// (unshift move todos os outros elementos para frente)
pilha.unshift(5)

// Mostra o array após inserir no início
console.log(pilha)